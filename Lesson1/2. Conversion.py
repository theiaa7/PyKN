num = 70
print(f"num value is {num} the data type is {type(num)}")

int2string = str(num)
print(f"int2string value is {int2string} the data type is {type(int2string)}")

int2float = float(num)
print(f"int2float value is {int2float} the data type is {type(int2float)}")

num2 = 3.78
float2int = int(num2)
print(f"float2int value is {float2int} the data type is {type(float2int)}")
num3 = float(float2int)
print(num3)